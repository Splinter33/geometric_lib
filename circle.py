
import math
'''
Добавление math для использование pi
'''

def area(r):
    return math.pi * r * r
'''
Принимает радиус круга - r.
Возвращает значение площади круга, вычисленной с помощью S = pi * r^2.
'''

def perimeter(r):
    return 2 * math.pi * r

